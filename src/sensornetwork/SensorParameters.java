package sensornetwork;

public class SensorParameters {
	private Double size;
	private Double weight;
	private Double wirelessRange;
	private Double sensorRange;
	private Double maxEnergy;

	public Double getSize() {
		return size;
	}

	public void setSize(Double size) {
		this.size = size;
	}

	public Double getWeight() {
		return weight;
	}

	public void setWeight(Double weight) {
		this.weight = weight;
	}

	public Double getWirelessRange() {
		return wirelessRange;
	}

	public void setWirelessRange(Double wirelessRange) {
		this.wirelessRange = wirelessRange;
	}

	public Double getSensorRange() {
		return sensorRange;
	}

	public void setSensorRange(Double sensorRange) {
		this.sensorRange = sensorRange;
	}

	public Double getMaxEnergy() {
		return maxEnergy;
	}

	public void setMaxEnergy(Double maxEnergy) {
		this.maxEnergy = maxEnergy;
	}
}
