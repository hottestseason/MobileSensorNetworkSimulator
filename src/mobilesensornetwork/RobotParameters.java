package mobilesensornetwork;

public class RobotParameters {
	Double size;
	Double weight;
	Double wirelessRange;
	Double sensorRange;
	Double maxSpeed;
	Double minSpeed;
	Double maxAcceleration;
	Double maxEnergy;

	public Double getSize() {
		return size;
	}

	public void setSize(Double size) {
		this.size = size;
	}

	public Double getWeight() {
		return weight;
	}

	public void setWeight(Double weight) {
		this.weight = weight;
	}

	public Double getWirelessRange() {
		return wirelessRange;
	}

	public void setWirelessRange(Double wirelessRange) {
		this.wirelessRange = wirelessRange;
	}

	public Double getSensorRange() {
		return sensorRange;
	}

	public void setSensorRange(Double sensorRange) {
		this.sensorRange = sensorRange;
	}

	public Double getMaxSpeed() {
		return maxSpeed;
	}

	public void setMaxSpeed(Double maxSpeed) {
		this.maxSpeed = maxSpeed;
	}

	public Double getMinSpeed() {
		return minSpeed;
	}

	public void setMinSpeed(Double minSpeed) {
		this.minSpeed = minSpeed;
	}

	public Double getMaxAcceleration() {
		return maxAcceleration;
	}

	public void setMaxAcceleration(Double maxAcceleration) {
		this.maxAcceleration = maxAcceleration;
	}

	public Double getMaxEnergy() {
		return maxEnergy;
	}

	public void setMaxEnergy(Double maxEnergy) {
		this.maxEnergy = maxEnergy;
	}
}
